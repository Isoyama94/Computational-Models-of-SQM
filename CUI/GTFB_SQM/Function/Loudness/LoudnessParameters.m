function [QL,E0,TQ_power,G_power,alph_new,A_new] = LoudnessParameters(Setting)
switch Setting.Op
    case 'Ap' % Applied acoustics
        CF = (10.^(Setting.ERBNnumber./21.4)-1)./0.00437;

        CF_TQ = [50 63 80 100 125 160 200 250 315 400 500 630 750 800 1000];
        TQ = [27.46 23.45 18.47 15.13 11.97 9.34 7.43 5.75 4.73 3.92 3.15 3.15 3.15 3.15 3.15];

        G_alph = [-25.0 -20.0 -15.0 -10.0 -5.0 -0.0];
        alph = [0.26692 0.25016 0.23679 0.22228 0.21055 0.20000];

        G_A = -25.0:0.5:0.0;
        A = [7.784 7.667 7.551 7.435 7.318 7.210 7.103 6.996 6.889 6.782 6.675 6.596 6.517 ...
            6.438 6.360 6.281 6.202 6.124 6.047 5.975 5.902 5.823 5.744 5.665 5.587 5.510 5.437 ...
            5.364 5.291 5.218 5.145 5.086 5.027 4.972 4.918 4.863 4.808 4.754 4.699 4.644 4.590 ...
            4.542 4.496 4.451 4.405 4.359 4.314 4.268 4.222 4.177 4.131];

        TQ_new = interpn(CF_TQ,TQ,CF,'spline');
        TQ_new(1,91:end) = 3.15;
        TQ_power = 10.^(TQ_new/10);
        G = 3.92-TQ_new;
        G_power = 10.^(G/10);

        alph_ref = 0.049;
        alph_new = interpn(G_alph,alph,G,'spline');
        alph_new = alph_new+alph_ref;

        A_new = interpn(G_A,A,G,'spline');
%         A_new = A_new+0.021;

        E0 = 0.9870; % Applied acoustics
        QL = 0.0806; % Aplide acoustiecs

    case 'INTERNOISE' % InterNoise
        CF = (10.^(Setting.ERBNnumber./21.4)-1)./0.00437;

        CF_TQ = [50 63 80 100 125 160 200 250 315 400 500 630 750 800 1000];
        TQ = [27.46 23.45 18.47 15.13 11.97 9.34 7.43 5.75 4.73 3.92 3.15 3.15 3.15 3.15 3.15];

        G_alph = [-25.0 -20.0 -15.0 -10.0 -5.0 -0.0];
        alph = [0.26692 0.25016 0.23679 0.22228 0.21055 0.20000];

        G_A = -25.0:0.5:0.0;
        A = [7.784 7.667 7.551 7.435 7.318 7.210 7.103 6.996 6.889 6.782 6.675 6.596 6.517 ...
            6.438 6.360 6.281 6.202 6.124 6.047 5.975 5.902 5.823 5.744 5.665 5.587 5.510 5.437 ...
            5.364 5.291 5.218 5.145 5.086 5.027 4.972 4.918 4.863 4.808 4.754 4.699 4.644 4.590 ...
            4.542 4.496 4.451 4.405 4.359 4.314 4.268 4.222 4.177 4.131];

        TQ_new = interpn(CF_TQ,TQ,CF,'spline');
        TQ_new(1,91:end) = 3.15;
        TQ_power = 10.^(TQ_new/10);
        G = 3.92-TQ_new;
        G_power = 10.^(G/10);

        alph_ref = 0.049;
        alph_new = interpn(G_alph,alph,G,'spline');
        alph_new = alph_new+alph_ref;

        A_new = interpn(G_A,A,G,'spline');
        A_new = A_new+0.021;

        E0 = 0.9870;
        QL = 1/20.9229738138593;
    case 'N03' % TVL
        if 1 == strcmp(Setting.TimeVarying,'ON')
            E0 = 0.999010378408762;
            QL = 1/12.0036564237917;
        elseif 1 == strcmp(Setting.TimeVarying,'OFF')
            E0 = 0.9870;
            QL = 1/16.7985493064845;
        else
            msg = 'Error: Chose Setting.TimeVarying "ON" or "OFF"';
            error(msg)
        end

        CF = (10.^(Setting.ERBNnumber./21.4)-1)./0.00437;

        CF_TQ = [50 63 80 100 125 160 200 250 315 400 500 630 750 800 1000];
        TQ = [27.46 23.45 18.47 15.13 11.97 9.34 7.43 5.75 4.73 3.92 3.15 3.15 3.15 3.15 3.15];

        G_alph = [-25.0 -20.0 -15.0 -10.0 -5.0 -0.0];
        p1 =  6.33598007956460*10^-5;
        p2 =  -2.00343787045027*10^-3;
        p3 =  0.247946420188235;

        alph = p1.*G_alph.^2 + p2.*G_alph + p3;
        alph(alph<0) = 0;

        G_A = -25.0:0.5:0.0;
        A = [7.784 7.667 7.551 7.435 7.318 7.210 7.103 6.996 6.889 6.782 6.675 6.596 6.517 ...
            6.438 6.360 6.281 6.202 6.124 6.047 5.975 5.902 5.823 5.744 5.665 5.587 5.510 5.437 ...
            5.364 5.291 5.218 5.145 5.086 5.027 4.972 4.918 4.863 4.808 4.754 4.699 4.644 4.590 ...
            4.542 4.496 4.451 4.405 4.359 4.314 4.268 4.222 4.177 4.131];

        TQ_new = interpn(CF_TQ,TQ,CF,'spline');
        TQ_new(1,91:end) = 3.15; % more 500 Hz (constants)
        TQ_power = 10.^(TQ_new/10);
        G = 3.92-TQ_new;
        G_power = 10.^(G/10);

        alph_new = interpn(G_alph,alph,G,'spline');

        A_new = interpn(G_A,A,G,'spline');
end
end

